                <div class="title-box">
                    <h3 class="title">防制詐欺因應之道</h3>
                </div>
                <ol style="padding-left:2em;">
                    <li>探究所有詐欺案中，以被害人「貪小失大」的原因居多，尤以近來流行刮刮樂、六合彩（金）之詐財案例中，甚有「貪小失更大」之事例發生，是以防詐第一要務為：「勿存貪念」。貪念是造成被騙的主因。</li>
                    <li>通常舉辦刮刮樂彩券贈獎活動之單位，必須有一家合法公司做信譽保證，並請政府財稅機關當見證。民眾應先向該保證公司或相關見證機關電話查詢，且查詢電話勿依照宣傳單上之號碼，而應透過104、105電話查號後再行查詢。</li>
                    <li>網路購物應注意網路商品與一般市價是否相當，若差距過大則風險較多，且應選擇有信譽之拍賣網站或購物網站，並瞭解欲交易之貨主之信用、風評等，而交易方式最好能以當面交易並銀貨兩訖的方式，絕對不要在未確認物品的情形之前就將款項匯出。</li>
                    <li>提款時請向自己熟悉之提款機提款，或儘量在銀行或郵局或其他金融機構內部提款機提款，避免在不明提款機或臨時設置之提款機提款，以防金融卡條碼遭側錄再複製卡片盜領。</li>
                    <li>如發現提款機有故障或提款有問題應向該提款機之銀行查詢，以防歹徒有機可乘。</li>
                    <li>正當公司所辦刮刮樂彩券贈獎活動，領取彩金是要先繳稅金。為避免受騙，可親自登門拜訪，以確定真假。</li>
                    <li>個人身分證、健保、信用卡及護、駕照等證件應妥善保管，勿輕易交予他人。發現遺失或破損時，應即時向有關機關報備並申請補發，做好檢查防護措施，以防止被非法冒用，因而權益受損。</li>
                    <li>金光黨行騙對象以教育程度較低、鄉村年老者居多。平時應常提醒他（她）們有關歹徒行騙手法及勿與陌生人閒談。其存款簿、印章應分開存放或交待家人代為保管。另金融業者遇到客戶（尤指老年人）有異常提領鉅款時，應機警主動查詢或通報警察到場瞭解實情。</li>
                    <li>重要文件、影本、郵局或銀行帳戶存摺（包括不用之存摺）、空白支票等資料，避免遺失或流出。對需要用簽（蓋）章作為鑑定辨識依據之文件，最好能以簽名代替印章，較能防止印章被仿冒或盜用致發生權益受損情事。</li>
                    <li>注意自己郵局、銀行或信用卡帳戶內金額多寡的改變，隨時與郵局、銀行保持聯繫。</li>
                    <li>收到他人所開支票時，應先考慮開戶（票）時間，可經由銀行徵信其開戶日期、往來情形及存款基數。開戶時間太短而金額龐大時更要特別留意。</li>
                    <li>參加民間互助會，應注意會首及其他會員的信用情形，對交付會首或會員之會款，應儘可能要求收款人出具簽章收據，以示鄭重與負責，並隨時注意每會開標情形，以確實瞭解互助會運作是否正常。</li>
                    <li>房屋買賣，要找信用可靠、有經驗、口碑良好或自己熟識的代書經手，對所交易之標的物，應先徵信其土地資料，瞭解其設定抵押狀況及貸款情形，並可向原屋主查詢，或利用電腦連線查詢該案情況，若情況有疑，應暫緩簽約。</li>
                    <li>遇有報稱親友傷病救助情事時，應先保持冷靜，然後求證，先打電話確認那間醫院，那個病床，並向相關親友探詢，才能釐清實情，避免受騙。</li>
                    <li>俗話說「十賭九輸」、「賭是無底深淵」，碰到老千勢必損失慘重，戒賭是免於受騙之上策。</li>
                    <li>遇公務人員執行職務時，除了從相關執行人員服飾及佩件認明外，應請其出示身分證件。</li>
                    <li>對於容易變現的貴重金飾等物品，輕易就能廉價買到，能不懷疑其中有詐？杜絕貪念是免於受騙的不二法門。</li>
                    <li>疾病醫療基本上是嚴謹的科學行為，有病就醫、對症下藥。盲目投醫或輕易相信他人介紹，而服用偏方或未經臨床實驗藥品，是一件非常冒險的事，且易為騙徒乘機詐財。</li>
                    <li>國人過於重視食補療效、喜歡買藥或亂服成藥等一些不正確觀念與習性，加上誤聽誇大不實的商品、醫藥廣告，是遭致不肖商人詐財的主因。</li>
                    <li>因迷信宗教信仰，以致對「神明」過份依賴，造成不法之徒有機可乘不當運用，利用宗教或巫術詐騙。國人應有所認知防範。</li>
                    <li>歹徒常利用假身分證行騙，為維護自我權益民眾若遺失身分證，應立即向警方報案，接著上警政署網站(http://www.npa.gov.tw)查詢是否已經完成遺失登錄。再到戶政單位申請掛失後，上網進入戶政司(http://www.ris.gov.tw/)「國民身分證領補換資料查詢」，確定戶政機關已經沒有舊的身分證後，再輸入新資料確認是否完成登錄。最後要記得經戶政機關簽章，確認無誤的「身分證補換申請書」影本，函送「金融聯合徵信中心」建檔，金融中心的地址是：台北市重慶南路一段二號十樓，電話是(02)23813939轉201～209。</li>
                    <li>以公司名義招攬旅遊或代辦信用卡等活動，應先向財政部、建設局及稅捐機關查詢該公司是否有立案，並按址向該公司訪查，多一分存疑，避免被騙上當。</li>
                    <li>學生打工應注意書面契約內容，如果有預扣工資（如先扣工資做為賠償的預備金）、工作未滿多少天不發薪、服務未滿預定期限要處罰、要求預繳保證金、要求簽訂放棄一切民事賠償條款、強迫加班條款或不加班扣錢，以及扣押身分證等事項，絕不可輕易簽約，並向學校或勞工行政單位反映。勞委會印製有「工讀生服務手冊」，同學可向勞委會索取。電話：（0800）211459或（02）8590-2866</li>
                </ol>
                以上所列，係針對最近較常發生之詐欺案類，略舉其防詐因應之道。詐欺案件之受害者，大部分都是眛於無知或因無助所致。要避免受騙，除不心存貪念外，多吸收資訊以增進常識，可藉他人經歷教訓，引為鑑戒。遇事遵行「停」、「聽」、「看」守則；也就是勿急、勿躁、多思考、詳查證，妥加研判處置，如此應可避免許多錯誤和損失。
