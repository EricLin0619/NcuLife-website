                    <dd>女學生在網路聊天室認識多名男性網友，冒稱某知名大學研究生，課餘兼職拍攝廣告，並將電視廣告模特兒照片寄給對方，最後假藉理由向男性網友借錢，款項到手後不知去向。</dd>
