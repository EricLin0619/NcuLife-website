                <div class="title-box" >
                    <h3 class="title">反詐騙宣導</h3>
                </div>
                近十年來財產犯罪人數中，最多者除了竊盜犯罪外，就是詐欺犯罪了。根據統計，犯罪人數從八十二年到九十一年間，共計一九一二六人。由此可見詐欺犯罪之多，社會大眾不可不隨時提高警覺，慎加預防。詐欺犯罪歹徒擅於利用人性的貪婪，以巧言令色配合時空因素，遂行詐騙技倆。為使民眾瞭解各類詐欺案例、手法特性及如何避免受騙，爰彙編本書供參考，引為前車之鑑，以減少詐欺案件發生。
                <div class="title-box">
                    <h3 class="title">常見詐騙犯罪型態與手法</h3>
                </div>
                <dl>

                    <dt>（一）刮刮樂、六合彩金詐欺</dt>
                    <ul>
                        <li>詐騙集團印製大量刮刮樂彩券寄出，收件人一刮皆中鉅額獎金，當被害人打電話查詢時，對方即要求預付百分之十五稅金，後歹徒接著又以彩金須會員才能領取，要求再繳會費及公司已代簽六合彩之簽注金，周而復始，連環詐財。</li>
                        <li>詐騙集團刊登或散發廣告，宣稱為某國際集團公司與大陸官方交涉，可獨家鎖控香港彩券局之六合彩明牌，如有失誤願理賠千萬元等為餌，向各地六合彩迷詐財。</li>
                        <li>詐騙集團將刮刮樂置於知名商品紙箱內，使消費者誤信是廠商的酬賓活動，並以刮刮樂上之電話查詢無誤後將稅金匯入指定帳戶內。</li>
                        <li>詐騙集團利用報紙刊登中獎名單，並在宣傳單上印有香港特區行政長官董建華的照片作為集團負責人，俾以取信民眾。</li>
                    </ul>
                    <dt>（二）信用卡詐欺</dt>
                    <ul>
                        <li>歹徒設法先行得知消費者信用卡內碼後，據以偽、變造該信用卡，再勾結商家大肆消費。</li>
                        <li>歹徒以偽造、拾得他人遺失之身分證，向銀行申請信用卡後盜刷。</li>
                        <li>歹徒在原申請人未收到銀行寄出的信用卡前，將其攔截後盜刷。</li>
                        <li>歹徒以空白信用卡，用打凸機、錄碼機、燙印機打上持卡人身分資料、卡號及發卡日期，複製猶如真品的信用卡，再和廠商勾結刷卡，嗣向銀行要求理賠後對分利益。</li>
                        <li>被害人利用信用卡在電腦網路上購物消費，致信用卡卡號遭到網路駭客入侵攔截，繼而被冒用盜刷。</li>
                    </ul>
                    <dt>（三）行動電話簡訊詐欺<br>
                    </dt>
                    <dd>歹徒利用電腦傳送「中轎車」、「中大獎」之手機簡訊，使手機簡訊接收者誤以為中獎，該詐騙集團即要求民眾必先提供稅金，以保有中獎之禮品，騙取民眾之錢財或在簡訊留下一組0941、0951、0204、0209等加值付費電話號碼，要你回電給他的情況，有的用戶會真的用手機回電給對方，對方可能會藉故跟你哈啦許久，藉機詐取電話費用。</dd>
                    <dt>（四）金融卡匯款方式詐欺</dt>
                    <dd>歹徒在一般媒體、網路或散發傳單以超低價價格販售賣相良好之商品，待民眾電話詢價時，即以時機不再須即刻以金融卡轉帳方式購買，再利用一般民眾不懂轉帳程序，而設計出一套繁複的操作順序指示，按照其指示操作後，轉成功的金額往往數十倍於原來之費用，以達到將一般民眾之存款詐轉的目的。</dd>
                    <dt>（五）網路購物詐欺</dt>
                    <dd>歹徒在網路上刊出非常低廉的商品誘使民眾匯款，再以劣品充數，交易完成後即避不見面。 <br>
                    </dd>
                    <dt>（六）網路銀行轉帳詐欺</dt>
                    <dd>歹徒在報紙刊登廣告或散發傳單，宣稱可幫助民眾貸款、加盟、購買法拍車等，要求被害人先行到其指定銀行開戶，存入相當之權利金或保證金，並設定電話語音約定轉帳帳戶（網路銀行服務及語音查詢帳戶餘額），然後歹徒再要求被害人提供語音查詢餘額密碼及身分證件、地址等相關資料以便確認，再行利用電話語音轉帳功能（網路電子交易）將被害人之存款轉帳領走</dd>
                    <dt>（七）提款機詐欺</dt>
                    <dd>歹徒利用民眾貪圖方便心理，在遊樂場或臨時市集旁放置假提款機或在提款機鍵盤套上假鍵盤，當民眾置入信用卡或金融卡或按鍵後側錄密碼，進而盜領存款。</dd>
                    <dt>（八）假權狀、證照文件詐欺</dt>
                    <dd>不法之徒利用精密手法，偽造、變造身分證或政府機關核發執照、權狀、文書等證件，實施各種詐欺行為。如以他人偽、變造身分證申領護照，或以假文件請領土地所有權狀等是。</dd>
                    <dt>（九）金光黨詐欺</dt>
                    <dd>以二人或三人一組，向被害人謊稱其中一人為傻子，身懷鉅款或金飾，激起被害人的貪念，以「扮豬吃老虎」騙取財物配合「調包」等手法，利用人性貪小便宜弱點，用假鈔或假金飾詐財。<br>
                    </dd>
                    <dt>（十）假元寶、金飾詐欺</dt>
                    <dd>一人或二人衣著普通，向被害人佯稱其手上有甫挖掘出土的元寶、金條或戒指等金飾，因需款應急，願以低價出售，引起受害人貪念而購買受騙。</dd>
                    <dt>（十一）票據詐欺（芭樂票詐欺）</dt>
                    <ul>
                        <li>在銀行開立支票存款帳戶，以空頭支票詐欺購得財貨，借貸或清償債務。</li>
                        <li>以他人名義設立帳戶領支票，或以他人名義開戶，領取支票販賣，供人開立空頭支票，以為詐欺之用。</li>
                    </ul>
                    <dt>（十二）虛設行號詐欺</dt>
                    <ul>
                        <li>刊登廣告虛設行號，廣招職員，以就業為餌，詐取就業保證金。</li>
                        <li>虛設行號出售統一發票，便利他人逃漏稅之詐欺犯罪。</li>
                        <li>虛設公司行號，以空頭支票對外購貨或舉債，達到詐財目的。</li>
                        <li>假稱經營事業利潤優厚，並以缺乏資金為由，利用人貪圖暴利之心理弱點，引誘入股，四處吸金詐財。</li>
                    </ul>
                    <dt>（十三）重利型投資詐欺</dt>
                    <dd>以投資重大土地開發或專利案之暴利行業為餌，初期給予投資者些許利益甜頭，待吸金達到一定飽和點就宣布倒閉潛逃，使參加投資者遭受錢財損失。</dd>
                    <dt>（十四）惡性倒閉詐欺</dt>
                    <dd>讓人誤信其公司之營業及財務狀況良好，藉以向外大舉進貨或借貸，隨後將財產化整為零，宣告倒閉，續以極低金額折成償還，並請被害人立據放棄其餘債權。此即俗稱之「破產詐欺」。</dd>
                    <dt>（十五）互助會詐財（倒會詐欺）</dt>
                    <dd>常見型態就是以會養會並自任會頭，暗自以會員名義連續標得會款，再捲款潛逃。</dd>
                    <dt>（十六）不動產售賣詐欺</dt>
                    <ul>
                        <li>一屋數賣：將已出售而未辦理所有權移轉登記之不動產，再次出售。</li>
                        <li>出售人將不動產讓給買受人，趁其未辦理移轉登記之際，復將該不動產設定抵押權，向抵押權人貸款，或將已設定抵押權之不動產以無抵押之價格出售。</li>
                        <li>以虛構事實刊登廣告誘人投資或預售房屋，以騙取定金。</li>
                    </ul>
                    <dt>（十七）假流當品詐欺</dt>
                    <dd>歹徒利用人多場所擺設金碧輝煌的高級飾品或用品，並有大招牌書明為某大當鋪流當品大拍賣，其實際價值往往為售價的十分之一以下。</dd>
                    <dt>（十八）坊間調查詐欺</dt>
                    <dd>歹徒於不特定地方或會場或說明會或大型集會或路邊，以某項調查為名，並以只要填寫資料即可獲紀念品為誘，使他人填入含身份證在內之私人資料，歹徒再利用這些資料去詐領信用卡或其他有價證券等。</dd>
                    <dt>（十九）保險詐欺</dt>
                    <ul>
                        <li>病患以行賄或其他不法方法取得不實之健康證明書，簽買人壽保險，旋即病故，由受益人領得保險金。</li>
                        <li>將投保之房屋、汽車等產物故意破壞或謊報失竊，致使保險標的滅失，以詐領保險金。</li>
                        <li>為親人、他人或自己簽買保險契約，而後謀害親人、他人或找人替死，以詐領保險金。</li>
                    </ul>
                    <dt>（二十）詐購財物詐欺</dt>
                    <dd>偽稱購買，利用商人售貨牟利及商業信賴心理，於取得財貨後逃匿。<br>
                    </dd>
                    <dt>（廿一）巫術或宗教詐欺</dt>
                    <dd>利用他人迷信心理，以鬼神之說恐嚇受害人，再偽稱能為人作法、消災去厄、解運、祈福等；以提供命相、卜卦等勞務，遂其「騙財騙色」之目的；或以建廟、寺等為名，向信徒募款詐財。</dd>
                    <dt>（廿二）重病醫藥詐欺</dt>
                    <dd>類似金光黨詐欺模式，以三至五人為一組，向罹患重病、痼疾者或其家屬，誑稱其中一人為名醫，並有珍貴名藥、偏方可醫治其病。趁被害人處於沮喪、絕望，抱著姑且一試心理，騙取巨額醫藥費。</dd>
                    <dt>（廿三）假身分詐欺</dt>
                    <dd>如假冒檢察官收紅包；冒用警察、刑警身分臨檢或辦案斂財；假藉公益團體騙取善款；假社工員以發放老人年金之名，騙取存摺、印章進而盜領存款；冒充星探向欲往演藝界發展少女，騙財騙色等皆是。</dd>
                    <dt>（廿四）勞務詐欺</dt>
                    <dd>假藉為人代辦事務或某特定勞務，要求預付報酬或因該工作所需之費用，詐取他人財務，如俗稱「司法黃牛」即是。</dd>
                    <dt>（廿五）居間詐欺</dt>
                    <dd>利用媒介雙方訂約機會，居間詐得他人財物。如偽稱介紹職業或介紹外籍、大陸新娘，從中騙取介紹費、保證金或代辦費等。</dd>
                    <dt>（廿六）打工陷阱詐欺</dt>
                    <dd>刊登廣告以高薪、工作輕鬆求「公主」、「男公關」或「影、歌星」為餌，向應徵者騙取訂金、保證金、置裝費或訓練費或車輛（替代保證金）等詐財。</dd>
                    <dt>（廿七）不法傳銷詐欺</dt>
                    <dd>不法業者以績效獎金及分紅制度為幌，實則行「老鼠會」傳銷商品方式向求職者詐騙錢財與勞力。</dd>
                    <dt>（廿八）婚姻、交友詐欺</dt>
                    <dd>謊稱自己未婚或離婚，利用他人急於交友尋找婚姻對象或續絃之際，以結婚、交友為餌，詐騙對方之財物後，藉詞拖延或一走了之。</dd>
                    <dt>（廿九）謊報傷病救急詐欺</dt>
                    <dd>向被害人謊稱其親人、朋友或同學遭逢車禍或其他重大變故，急需用款，在被害人一時心慌、情急不及思考、查證情況下，倉促間誤信歹徒而受騙破財。</dd>
                    <dt>（三十）老千集團詐欺</dt>
                    <dd>由三至五名精於賭術者設局詐賭，以高超賭術或賭具，利用最新科學電子儀器，向民眾詐財。</dd>
                    <dt>（三十一）假貨騙售詐欺</dt>
                    <dd>以出售不具真實價值之物品，以詐取財物，如出售偽藥、偽造劣酒牟利等。</dd>
                    <dt>（三十二）佯稱代辦貸款詐欺</dt>
                    <dd>於報上刊登廣告，佯稱可為急需用錢者代辦貸款事宜，藉此向被害人騙取律師（代書）代辦手續費、保證金等。</dd>
                    <dt>（三十三）假稱傷病、貧困詐欺</dt>
                    <dd>佯裝傷病，謊稱潦倒困苦，藉此博人同情，騙取車資、醫藥或生活費等。 </dd>
                    <dt>（三十四）超收拖吊車輛費用詐欺</dt>
                    <dd>被害人車輛在公路（尤為高速公路）拋錨，拖吊業者於車輛拖吊或修復後，向被害人索取顯不相當之高額費用。 </dd>
                    <dt>（三十五）度量衡詐欺</dt>
                    <dd>商家出售貨品或提供服務時，將定程法碼之衡器動手腳，如賣蔬果短少斤兩、計程車跑錶加速跳動等，多賺價款。</dd>
                    <dt>（三十六）登廣告假脫售詐欺</dt>
                    <dd>佯稱BMW、BENZ高級中古車、名貴圖畫或貴重金飾、音響等物品急需脫售，向被害人收取訂金，並給予一張等額的支票為憑，另以需辦理過戶或其他理由騙取被害人身份證件、印鑑，即不知去向，續行詐騙。</dd>
